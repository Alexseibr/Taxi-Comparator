// Короткий формат адреса в Минске.
// На входе — длинная Nominatim-строка вроде:
//   "18, 3-й Подольский переулок, Сельхозпосёлок, Советский район, Минск, 220040, Беларусь"
// На выходе — короткий человеко-читаемый вариант:
//   "3-й Подольский переулок, 18"
//
// Алгоритм:
//   1. Разбиваем по запятым, чистим пробелы.
//   2. Отбрасываем «шум»: район, область, страну, индекс, "Минский район",
//      "Сельсовет" и пр. Эти токены проще искать по списку маркеров.
//   3. Первый осмысленный токен «дом» (число или число+буква типа "9А") —
//      запоминаем как номер дома.
//   4. Следующий — имя улицы (улица/переулок/проспект/бульвар/тракт/...).
//   5. Возвращаем "{street}, {house}" если есть номер, иначе просто "{street}".
//
// Если структура не распозналась — возвращаем первые 1-2 значимых элемента
// исходной строки.

const NOISE_RE = [
  /^минск$/i,
  /^мінск$/i,
  /^беларусь$/i,
  /^беларусь$/i,
  /^belarus$/i,
  /^минский район$/i,
  /^мінскі раён$/i,
  /^минская область$/i,
  /^мінская вобласць$/i,
  /район$/i,           // "Советский район"
  /раён$/i,
  /сельсовет$/i,
  /сельсавет$/i,
  /сельский совет$/i,
  /агрогородок$/i,
  /аграгарадок$/i,
  /^\d{6}$/,           // почтовый индекс 220040
  /микрорайон$/i,      // "Сельхозпосёлок" — обычно микрорайон/посёлок
  /мікрараён$/i,
  /посёлок$/i,
  /пасёлак$/i,
  /^[xX][a-z0-9]{2,8}$/i, // мусор-токены
];

const HOUSE_RE = /^\d+[А-Яа-яA-Za-z\-/]*$/; // 18, 9А, 12/3, 5к1
const STREET_HINT_RE =
  /(улица|переулок|проспект|бульвар|шоссе|тракт|набережная|тупик|проезд|линия|тракт|площадь|сквер|просп\.|ул\.|пер\.|пр\.)/i;

function isNoise(token: string): boolean {
  const t = token.trim();
  if (!t) return true;
  return NOISE_RE.some((re) => re.test(t));
}

/**
 * Главная функция: возвращает короткий формат «улица, дом» для адреса в Минске.
 * Если на вход уже коротко — вернёт как есть.
 */
export function shortenMinskAddress(displayName: string): string {
  if (!displayName) return "";
  const raw = displayName.split(",").map((s) => s.trim()).filter(Boolean);
  if (raw.length <= 1) return displayName.trim();

  // Чистим от шума.
  const meaningful = raw.filter((t) => !isNoise(t));

  if (meaningful.length === 0) return raw[0]; // совсем странный адрес

  // Случай 1: первый токен — номер дома, второй — улица.
  if (HOUSE_RE.test(meaningful[0]) && meaningful.length >= 2) {
    const house = meaningful[0];
    const street = meaningful[1];
    return `${street}, ${house}`;
  }

  // Случай 2: первый токен — имя улицы (содержит "улица"/"переулок"/...).
  // Второй может быть номером дома, если есть.
  if (STREET_HINT_RE.test(meaningful[0])) {
    if (meaningful.length >= 2 && HOUSE_RE.test(meaningful[1])) {
      return `${meaningful[0]}, ${meaningful[1]}`;
    }
    return meaningful[0];
  }

  // Случай 3: непонятная структура (POI, метро, ТЦ) — берём первый-второй
  // значимые токены без разделения по «улица + дом».
  if (meaningful.length === 1) return meaningful[0];
  // Если второй токен похож на район/город — оставляем только первый.
  if (/минск|город|район|область/i.test(meaningful[1])) {
    return meaningful[0];
  }
  return `${meaningful[0]}, ${meaningful[1]}`;
}
